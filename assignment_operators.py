num = 10

print("Assignment Operators in Python")
print("-----------------------------")

print("Initial value of num:", num)
num += 5
print("After += 5:", num)
num -= 3
print("After -= 3:", num)
num *= 2
print("After *= 2:", num)
num /= 4
print("After /= 4:", num)
num %= 3
print("After %= 3:", num)

